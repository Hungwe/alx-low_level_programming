/**
 * _strcpy - Return a segmented string using pointers
 * @dest: Initial segment pointer
 * @src: Pointer for bytes to accept from
 *
 * Return: string copy
 */

char *_strcpy(char *dest, char *src)
{
char *a;

a = dest + *src;
return (a);
}
