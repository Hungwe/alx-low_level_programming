/**
 * _memset - Replaces n bytes with a constant byte
 * @s: Input pointer
 * @b: The byte to repeat
 * @n: Number of bytes
 *
 * Return: Pointer
 */

char *_memset(char *s, char b, unsigned int n)
{
char *y;
y = memset(s, b, n);
return (y);
}
