/**
 * _pow_recursion - Return x raised to the power of y
 * @x: Base
 * @y: Exponent
 *
 * Return: Number
 */

int _pow_recursion(int x, int y)
{
if (y < 0)
{
return (-1);
}
else if (y == 0)
{
return (1);
}
else
{
return (x * _pow_recursion(x, y - 1));
}
}
